// Function to get the Google Drive link
export const getNFTDownloadLink = (nftNumber: string): string => {
    console.log(`Input NFT Number: ${nftNumber}`); // Debug: Log the input
    const sanitizedNFTNumber = nftNumber.trim(); // Clean any extra spaces

    // Validate input number
    const num = parseInt(sanitizedNFTNumber);
    if (isNaN(num) || num < 1 || num > 9998) {
        throw new Error(`Invalid NFT number: ${nftNumber}`);
    }

    // Fetch file ID from mapping
    const fileId = NFT_FILE_IDS[sanitizedNFTNumber];
    if (!fileId) {
        throw new Error(`No file ID found for NFT #${sanitizedNFTNumber}`);
    }

    // Return the Google Drive download link
    return `https://drive.google.com/file/d/${fileId}/view?usp=sharing`;
};

